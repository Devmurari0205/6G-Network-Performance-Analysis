    "    fig2 = px.scatter(\n",
    "        df_filtered,\n",
    "        x=\"Packet_Loss\",\n",
    "        y=\"Error_Rate\",\n",
    "        color=\"Risk_Level\"\n",
    "    )\n",
    "    st.plotly_chart(fig2, use_container_width=True)\n",
    "\n",
    "# -----------------------------\n",
    "# EFFICIENCY ANALYSIS\n",
    "# -----------------------------\n",
