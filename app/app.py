    "import streamlit as st\n",
    "import pandas as pd\n",
    "import numpy as np\n",
    "import plotly.express as px\n",
    "\n",
    "# -----------------------------\n",
    "# PAGE CONFIG\n",
    "# -----------------------------\n",
    "st.set_page_config(page_title=\"6G Network Dashboard\", layout=\"wide\")\n",
    "\n",
    "st.title(\"📡 6G Network Performance Analysis\")\n",
    "st.markdown(\"Smart Manufacturing | Advanced Analytics Dashboard\")\n",
    "\n",
    "# -----------------------------\n",
    "# LOAD DATA\n",
    "# -----------------------------\n",
    "df = pd.read_csv(r\"C:\\united_inteship\\Un_project\\Thales_Group_Manufacturing01.csv\")\n",
    "\n",
    "# -----------------------------\n",
    "# DATA PREPROCESSING\n",
    "# -----------------------------\n",
    "# Risk Category\n",
    "def risk_category(row):\n",
    "    if row[\"Network_Latency_ms\"] > 30 or row[\"Packet_Loss\"] > 5:\n",
    "        return \"Critical\"\n",
    "    elif row[\"Network_Latency_ms\"] > 15:\n",
    "        return \"Warning\"\n",
    "    else:\n",
    "        return \"Normal\"\n",
    "\n",
    "df[\"Risk_Level\"] = df.apply(risk_category, axis=1)\n",
    "\n",
    "# -----------------------------\n",
    "# SIDEBAR FILTERS\n",
    "# -----------------------------\n",
    "st.sidebar.header(\"🔍 Filters\")\n",
    "\n",
    "operation_mode = st.sidebar.multiselect(\n",
    "    \"Operation Mode\",\n",
    "    df[\"Operation_Mode\"].unique(),\n",
    "    default=df[\"Operation_Mode\"].unique()\n",
    ")\n",
    "\n",
    "efficiency = st.sidebar.multiselect(\n",
    "    \"Efficiency Status\",\n",
    "    df[\"Efficiency_Status\"].unique(),\n",
    "    default=df[\"Efficiency_Status\"].unique()\n",
    ")\n",
    "\n",
    "risk = st.sidebar.multiselect(\n",
    "    \"Risk Level\",\n",
    "    df[\"Risk_Level\"].unique(),\n",
    "    default=df[\"Risk_Level\"].unique()\n",
    ")\n",
    "\n",
    "df_filtered = df[\n",
    "    (df[\"Operation_Mode\"].isin(operation_mode)) &\n",
    "    (df[\"Efficiency_Status\"].isin(efficiency)) &\n",
    "    (df[\"Risk_Level\"].isin(risk))\n",
    "]\n",
    "\n",
    "# -----------------------------\n",
    "# KPI SECTION\n",
    "# -----------------------------\n",
    "st.subheader(\"📊 Key Performance Indicators\")\n",
    "\n",
    "col1, col2, col3, col4 = st.columns(4)\n",
    "\n",
    "col1.metric(\"Avg Latency (ms)\", round(df_filtered[\"Network_Latency_ms\"].mean(), 2))\n",
    "col2.metric(\"Avg Packet Loss (%)\", round(df_filtered[\"Packet_Loss\"].mean(), 2))\n",
    "col3.metric(\"Avg Production Speed\", round(df_filtered[\"Production_Speed_units_per_hr\"].mean(), 2))\n",
    "col4.metric(\"Avg Error Rate (%)\", round(df_filtered[\"Error_Rate\"].mean(), 2))\n",
    "\n",
    "# -----------------------------\n",
    "# RISK DISTRIBUTION\n",
    "# -----------------------------\n",
    "st.subheader(\"🚨 Network Risk Distribution\")\n",
    "\n",
    "risk_count = df_filtered[\"Risk_Level\"].value_counts().reset_index()\n",
    "risk_count.columns = [\"Risk_Level\", \"Count\"]\n",
    "\n",
    "fig_risk = px.pie(\n",
    "    risk_count,\n",
    "    names=\"Risk_Level\",\n",
    "    values=\"Count\",\n",
    "    title=\"Risk Level Distribution\"\n",
    ")\n",
    "st.plotly_chart(fig_risk, use_container_width=True)\n",
    "\n",
    "# -----------------------------\n",
    "# LATENCY vs SPEED\n",
    "# -----------------------------\n",
    "col5, col6 = st.columns(2)\n",
    "\n",
    "with col5:\n",
    "    st.subheader(\"📈 Latency vs Production Speed\")\n",
    "    fig1 = px.scatter(\n",
    "        df_filtered,\n",
    "        x=\"Network_Latency_ms\",\n",
    "        y=\"Production_Speed_units_per_hr\",\n",
    "        color=\"Efficiency_Status\"\n",
    "    )\n",
    "    st.plotly_chart(fig1, use_container_width=True)\n",
